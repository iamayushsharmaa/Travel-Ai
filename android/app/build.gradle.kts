def dotenvFile = rootProject.file(".env")
def env = [:]
if (dotenvFile.exists()) {
    dotenvFile.eachLine { line ->
        line = line.trim()
        if (!line.startsWith("#") && line.contains("=")) {
            def (key, value) = line.split("=", 2)
            env[key.trim()] = value.trim()
        }
    }
}

